# str 타입의 연산자-멤버연산자
# - 문법: 왼쪽 in 오른쪽
# - 기능: 왼쪽 데이터가 오른쪽에 존재하는 요소/원소 
#         여부를 판단함 => True/False
# ------------------------------------------------
# in 연산자
print(f'"P" in "Price is 1000": {"P"in "Price is 1000"}')
print(f'"p" in "Price is 1000": {"p"in "Price is 1000"}')
print(f'"1" in "12345": {"1"in "12345"}')

# not in (in 연산자 반대)
print(f'"P" not in "Price is 1000": {"P"not in "Price is 1000"}')
print(f'"p" not in "Price is 1000": {"p"not in "Price is 1000"}')
print(f'"1" not in "12345": {"1"not in "12345"}')