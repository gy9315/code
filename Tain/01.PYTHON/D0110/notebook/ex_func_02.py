# 반환값/리턴값이 여러개인 함수
# 기능: 2개 정수에 대한 덧셈, 뺄셈, 곱셈, 나눗셈
# 이름: calc
# 변수: x,y
# 값: ~~
def calc(x,y):
    dan1=''
    dan1=dan1+f'{x} + {y} = {x+y}\n'.center(12)
    dan1=dan1+f'{x} + {y} = {x-y}\n'.center(12)
    dan1=dan1+f'{x} X {y} = {x*y}\n'
    if y==0: 
        dan1=dan1+'잘못된 인수가 입력되었습니다.\n'
    else:dan1=dan1+f'{x} / {y} = {round(x/y,2)}\n'
    return dan1
print(calc(5,3))

