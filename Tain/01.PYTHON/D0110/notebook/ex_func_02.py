# 기능: 사용자가 원하는 단의 구구단 출력해주는 기능
# 이름: dan
# 변수: x
# 값: x X 1 ~ 9 = x*1~9
def dan(x):
    dan1=''
    dan1=dan1+f'{"":4}{x}단\n'
    for y in range(1,10):
        dan1=dan1+f'{x} X {y} = {x*y}\n'.center(11)
    return dan1
