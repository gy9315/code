# 문자열 사용하기
hello='hello, world!'
print(hello)
hello='안녕하세요'
print(hello)
hello="hello, world!"
print(hello)
hello='''hello python!'''
print(hello)
hello="""python programming"""
print(hello)
# 여러 줄로 된 문자열 사용하기
hello='''Hello, world!
안녕하세요
Python입니다.'''
print(hello)
# 문자열 안에 작은 따옴표나 큰 타옴표 포함하기
s="python isn't difficult"
print(s)
s='He said "python is east'
print(s)
# [질문]
s=""""hello", python""" # "hello", python
print(s)
s="""hello, "python""""" # hello, "python
print(s)
